DATA SEGMENT
NOTIC DB "Please input the word !",0AH,0DH
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
MOV DS,AX
MOV CX,19H
LEA BX,[NOTIC]
AA0: MOV DL,[BX] ;输出提示
MOV AH,2
INT 21H
INC BX
LOOP AA0
AA1: MOV AH,1 ;读入回显
INT 21H
CMP AL,1BH ;是 ESC 跳转
JZ AA3 ;判断是小写就转换 否则不变
CMP AL,61H ;判断是不是小写
JS AA2
CMP AL,7AH
JNS AA2
SUB AL,20H ;转换成大写
AA2: MOV DL,AL
MOV AH,2
INT 21H
LOOP AA1
AA3: MOV AH,4CH
INT 21H
CODE ENDS
END START